def normalize_ui_lang(value: str | None) -> str:
    lang = (value or "").strip().lower()
    return lang if lang in SUPPORTED_UI_LANGS else "en"

def tr(lang: str, en: str, zh: str) -> str:
    if normalize_ui_lang(lang) != "zh":
        return en
    return ZH_I18N.get(en, en)

